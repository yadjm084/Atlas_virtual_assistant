"""## From MFCC Matrices to Fixed-Size Speaker Vectors

In our Activity 1 work, we already used **MFCCs** as the main audio representation. However, MFCC extraction produces a matrix of shape `(n_mfcc, time_frames)`, which means that each recording is represented by multiple feature values over time rather than by one fixed-size vector.

For the user verification module, we need a compact **voice profile** representation for each recording so that we can compare a new test recording against the enrolled users. A simple and effective way to do this is to summarize each MFCC coefficient over time using:
- its **mean**: **mean** of each MFCC coefficient over time
- its **standard deviation**: **standard deviation** of each MFCC coefficient over time

This produces one fixed-size feature vector per recording.  
With 13 MFCC coefficients, the final vector contains:
- 13 mean values
- 13 standard deviation values  
for a total of **26 features**

These vectors will later be used to:
- build one enrollment profile per speaker
- compare test recordings against those profiles


This makes it a suitable first approach for our project, especially since our proposal is based on creating a small voice profile for each of the three enrolled users and comparing new recordings against those saved profiles.
MFCCs are extracted as a matrix with shape `(n_mfcc, time_frames)`, where the number of time frames can vary across recordings.

"""

# ==============================
# STEP 19 - Convert MFCCs to a fixed-size vector
# ==============================

def mfcc_to_fixed_vector(mfcc):
    """
    Convert an MFCC matrix of shape (n_mfcc, time_frames)
    into a fixed-size feature vector.

    We summarize each MFCC coefficient across time using:
    - mean
    - standard deviation

    Returns:
        feature_vector (np.array): shape = (2 * n_mfcc,)
    """

    # Mean of each MFCC coefficient over time
    mfcc_means = np.mean(mfcc, axis=1)

    # Standard deviation of each MFCC coefficient over time
    mfcc_stds = np.std(mfcc, axis=1)

    # Concatenate means and stds into one fixed vector
    feature_vector = np.concatenate([mfcc_means, mfcc_stds])

    return feature_vector

