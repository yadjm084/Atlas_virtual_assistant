def extract_speaker_vector(file_path, target_sr=16000, target_length=40000, n_mfcc=13):
    """
    Full pipeline:
    1. Load and preprocess the audio
    2. Extract MFCCs
    3. Convert MFCCs to a fixed-size vector

    Returns:
        feature_vector (np.array): shape = (2 * n_mfcc,)
    """

    signal, sr = load_and_preprocess_audio(
        file_path,
        target_sr=target_sr,
        target_length=target_length
    )

    mfcc = extract_mfcc(signal, sr, n_mfcc=n_mfcc)
    feature_vector = mfcc_to_fixed_vector(mfcc)

    return feature_vector

# ==============================
# NEW STEP - Speaker vector extraction with optional augmentation
# ==============================

def extract_augmented_speaker_vectors(
    file_path,
    target_sr=16000,
    target_length=40000,
    n_mfcc=13,
    noise_factors=None
):
    """
    Extract the original speaker vector plus optional noisy augmented versions.

    Args:
        file_path (str): path to the audio file
        noise_factors (list or None): list of noise levels to apply

    Returns:
        vectors (list of np.array): list of speaker vectors
    """
    if noise_factors is None:
        noise_factors = []

    # Load and preprocess original signal
    signal, sr = load_and_preprocess_audio(
        file_path=file_path,
        target_sr=target_sr,
        target_length=target_length
    )

    vectors = []

    # Original vector
    mfcc = extract_mfcc(signal, sr, n_mfcc=n_mfcc)
    original_vector = mfcc_to_fixed_vector(mfcc)
    vectors.append(original_vector)

    # Augmented vectors
    for noise_factor in noise_factors:
        noisy_signal = add_white_noise(signal, noise_factor=noise_factor)
        noisy_mfcc = extract_mfcc(noisy_signal, sr, n_mfcc=n_mfcc)
        noisy_vector = mfcc_to_fixed_vector(noisy_mfcc)
        vectors.append(noisy_vector)

    return vectors

