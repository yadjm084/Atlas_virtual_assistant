from transformers import BertModel
import torch.nn as nn
class JointIntentSlotModel(nn.Module):
    def __init__(self, num_intents, num_slots):
        super().__init__()
        self.bert = BertModel.from_pretrained("bert-base-uncased")
        hidden = self.bert.config.hidden_size  # 768
        self.intent_head = nn.Linear(hidden, num_intents)
        self.slot_head   = nn.Linear(hidden, num_slots)
        self.dropout     = nn.Dropout(0.1)

    def forward(self, input_ids, attention_mask):
        outputs = self.bert(input_ids=input_ids, attention_mask=attention_mask)
        cls_output    = self.dropout(outputs.last_hidden_state[:, 0, :])
        token_outputs = self.dropout(outputs.last_hidden_state)
        intent_logits = self.intent_head(cls_output)
        slot_logits   = self.slot_head(token_outputs)

        return intent_logits, slot_logits


