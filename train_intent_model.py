class IntentSlotDataset(Dataset):
    def __init__(self, data, tokenizer, intent2id, slot2id, max_len=64):
        self.data = data
        self.tokenizer = tokenizer
        self.intent2id = intent2id
        self.slot2id = slot2id
        self.max_len = max_len
    def __len__(self):
        return len(self.data)
    def __getitem__(self, idx):
        item = self.data[idx]
        words = item["text"].lower().split()
        intent = item["intent"]
        slots_map = item["slots"]  # {word_index: slot_label}

        # Build word-level slot list
        word_slots = []
        for i in range(len(words)):
            word_slots.append(slots_map.get(i, "O"))

        # Tokenize with word alignment
        encoding = self.tokenizer(
            words,
            is_split_into_words=True,
            truncation=True,
            max_length=self.max_len,
            padding="max_length",
            return_tensors="pt"
        )

        # Align slot labels to subword tokens
        word_ids = encoding.word_ids()
        slot_ids = []
        prev_word_id = None
        for word_id in word_ids:
            if word_id is None:
                slot_ids.append(-100)  # special token, ignored in loss
            elif word_id != prev_word_id:
                slot_ids.append(self.slot2id[word_slots[word_id]])
            else:
                # continuation subword: use I- version if available, else -100
                label = word_slots[word_id]
                if label.startswith("B-"):
                    i_label = "I-" + label[2:]
                    slot_ids.append(self.slot2id.get(i_label, -100))
                else:
                    slot_ids.append(self.slot2id[label])
            prev_word_id = word_id

        return {
            "input_ids": encoding["input_ids"].squeeze(),
            "attention_mask": encoding["attention_mask"].squeeze(),
            "intent_label": torch.tensor(self.intent2id[intent]),
            "slot_labels": torch.tensor(slot_ids)
        }

