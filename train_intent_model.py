for epoch in range(EPOCHS):
    total_loss = 0
    for batch in loader:
        optimizer.zero_grad()

        intent_logits, slot_logits = model(
            batch["input_ids"],
            batch["attention_mask"]
        )

        loss_intent = intent_loss_fn(intent_logits, batch["intent_label"])
        loss_slot   = slot_loss_fn(
            slot_logits.view(-1, len(SLOT_LABELS)),
            batch["slot_labels"].view(-1)
        )
        loss = loss_intent + loss_slot
        loss.backward()
        optimizer.step()
        total_loss += loss.item()

    print(f"Epoch {epoch+1}/{EPOCHS} — loss: {total_loss:.4f}")


